// Build the Ask Intel corpus ("intel pack") the chat backend answers from.
//
// Concatenates the OKF markdown knowledge base (repo-root knowledge/, which sits
// OUTSIDE dashboard/ — Vercel's deploy root) into one committed TS module the
// serverless function imports, so the function never reads across the deploy root
// at runtime.
//
// Full markdown on purpose: unlike scripts/project_okf.py (which projects only each
// offering's Assessment + citations and skips research/ entirely), the chat needs the
// Detail prose, the teardowns, and the pricing profiles. Order is sorted by path so
// the cached system prompt stays byte-stable and Anthropic prompt caching keeps
// hitting — no timestamps, no randomness.
//
// Run: `npm run build:pack` (also runs in `prebuild`). The output is committed.
import { readdirSync, readFileSync, writeFileSync, statSync } from 'node:fs';
import { join, relative, resolve, basename } from 'node:path';

const KNOWLEDGE = resolve(import.meta.dirname, '../../knowledge');
const OUT = resolve(import.meta.dirname, '../api/_intel-pack.ts');
const EXCLUDE = new Set(['index.md', 'log.md']); // reserved, not concept docs

function walk(dir) {
  const out = [];
  for (const name of readdirSync(dir)) {
    const p = join(dir, name);
    if (statSync(p).isDirectory()) out.push(...walk(p));
    else if (name.endsWith('.md') && !EXCLUDE.has(name)) out.push(p);
  }
  return out;
}

// Scalar-only frontmatter reader (mirrors scripts/okf_parser.py + evals/lib/corpus.ts).
function frontmatter(text) {
  const m = text.match(/^---\n([\s\S]*?)\n---/);
  const fm = {};
  if (!m) return fm;
  for (const line of m[1].split('\n')) {
    const kv = line.match(/^([A-Za-z_][\w-]*):\s*(.+)$/);
    if (kv) fm[kv[1]] = kv[2].replace(/^["'[]|["'\]]$/g, '').trim();
  }
  return fm;
}

function titleFor(fm, path) {
  if (fm.type === 'Offering' && fm.competitor && fm.capability) return `${fm.competitor} · ${fm.capability}`;
  return fm.title || fm.topic || fm.slug || basename(path, '.md');
}

const files = walk(KNOWLEDGE).sort((a, b) =>
  relative(KNOWLEDGE, a).localeCompare(relative(KNOWLEDGE, b))
);

const docs = files.map((p) => {
  const text = readFileSync(p, 'utf8');
  const fm = frontmatter(text);
  const attrs = [`type="${fm.type ?? 'Doc'}"`, `title="${titleFor(fm, p).replace(/"/g, "'")}"`];
  if (fm.competitor) attrs.push(`competitor="${fm.competitor}"`);
  if (fm.capability) attrs.push(`capability="${fm.capability}"`);
  return `<document ${attrs.join(' ')}>\n${text.trim()}\n</document>`;
});

const pack = docs.join('\n\n');
const header =
  '// GENERATED by scripts/build-intel-pack.mjs — do not edit by hand.\n' +
  '// The Ask Intel chat corpus: the full OKF markdown knowledge base concatenated\n' +
  '// into one string. Regenerate with `npm run build:pack` when knowledge/ changes.\n\n';
writeFileSync(OUT, `${header}export const INTEL_PACK = ${JSON.stringify(pack)};\n`);
console.log(
  `intel pack: ${docs.length} docs, ${pack.length} chars → ${relative(resolve(import.meta.dirname, '..'), OUT)}`
);
